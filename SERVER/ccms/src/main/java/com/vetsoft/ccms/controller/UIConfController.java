	@RequestMapping(value = "/hafe_open_connections", method = RequestMethod.GET)
	public String clearHafeOpenedConnections(){
		
		Set<String> keys = MainBootApp.hafe_opened_channels_list.keySet();
		
		for(String key : keys){
			ChannelDetails ctx = MainBootApp.hafe_opened_channels_list.get(key);
			try {
				LOG.error("CLOSE HAFE OPENED CONNECTION : " + key  + " IP : "+ ctx.ip);
				serverHandler.closeHafeOpenedConnections(ctx.ctx);
				MainBootApp.hafe_opened_channels_list.remove(key);
			} catch (Exception e) {
				LOG.error("ERROR WHILE SENDING HAFE OPENED CONNECTION CLOSE EVENT : "+e.getMessage() + " IP : "+ ctx.ip);
			}
		}
		return "DONE";
	}
