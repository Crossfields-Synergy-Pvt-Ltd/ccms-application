	/*
	 * This function will send dcu  conf change request to device 
	 * this will be invoked from the UI, 
	 * node_data : is the dcu configuration data need to sync with device
	 * file_identifier : file id of the node conf generated by UI
	 * 
	 * this info will be stored in DB and retried by the scheduler in case of failure.
	 */
	@RequestMapping(value = "/sys_conf", method = RequestMethod.GET)
	public String syncSysConf(@RequestParam("dcu_id") String dcu_id, 
			@RequestParam("buffer") String buffer,
			@RequestParam("dcu_identifier") String dcu_identifier, 
			@RequestParam("node_data") String node_data, 
			@RequestParam("file_idetifier") String file_idetifier
			) {
		
		LOG.info("SYS CONF SYNC REQUEST FROM UI : "+ dcu_id + " "+ buffer);
		try{
			
			String CRC = Integer.toHexString(BaseUtil.calculateCRC(BaseUtil.convertHexToString(buffer.toString()).toCharArray(), BaseUtil.convertHexToString(buffer.toString()).length()));
			String chnage_request_packet = buffer+CRC;
			
		//	serverHandler.sendMessageToClient(buffer+CRC, dcu_id, 1);
			
			serverHandler.sendDcuConfSyncMesageToClient(chnage_request_packet, dcu_id, file_idetifier, node_data, dcu_identifier);
		}catch(Exception e){
			LOG.error("Exception while processing sysnc sys conf for device : "+ dcu_id);
		}
		LOG.info("SYS SYNC DONE SUCCESSFULLY");
		return "OK";
	}
	
	
	
	
